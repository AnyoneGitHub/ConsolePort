local function GetSize(sourceSize, subTexture)
	local width, height = unpack(sourceSize)
	local x1, x2, y1, y2 = unpack(subTexture)
	return {
		(x2*width)-(x1*width),
		(y2*height)-(y1*height),
	}
end

local function HideHighlight(self)
	self:GetHighlightTexture():Hide()
end

local function ShowHighlight(self)
	self:GetHighlightTexture():Show()
end


function ConsolePort:CreateAtlasButton(parent, source, subTexture, resizeParent, blendMode, hideHighlight)
	local fullTexture = db.Atlas[source]
	local textures = {}
	for i=1, 4 do
		local texture = fullTexture[subTexture..i]
		if texture then
			tinsert(textures, { coords = texture, state = db.Atlas.TextureType[i] })
		end
	end
	for num, info in pairs(textures) do
		local texture = parent:CreateTexture()
		texture:SetTexture(path..source)
		texture:SetTexCoord(unpack(info.coords))
		texture:SetAllPoints(parent)
		parent[prefix..info.state..suffix](parent, texture, num == 1 and blendMode)
	end
	if hideHighlight == true then
		parent:HookScript("OnMouseDown", HideHighlight)
		parent:HookScript("OnMouseUp", ShowHighlight)
	end
	if resizeParent == true then
		parent:SetSize(unpack(GetSize(db.Atlas.Size[source], textures[1].coords)))
	end
end

function ConsolePort:CreateAtlasTexture(parent, source, subTexture, layer, name, resizeParent)
	local fullTexture = db.Atlas[source]
	local textureName = name or subTexture
	local subTexture = fullTexture[subTexture]
	local texture = parent:CreateTexture(nil, layer)
	texture:SetTexture(path..source)
	texture:SetTexCoord(unpack(subTexture))
	texture:SetAllPoints(parent)
	parent[textureName] = texture
	if resizeParent == true then
		parent:SetSize(unpack(GetSize(db.Atlas.Size[source], subTexture)))
	end
	return texture
end